package com.bbeltranl.evaluation.model;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import jakarta.persistence.*;
import lombok.Data;

import java.util.UUID;

/**
 * Represents a phone entity with associated attributes.
 * <p>
 * This class is used to store phone-related information, including the phone number,
 * city code, and country code. It is annotated with JPA and Jackson annotations for
 * database persistence and JSON serialization/deserialization.
 * </p>
 *
 * @author bbeltranl
 * @version 1.0
 * @since 2024-06-13
 */
@Data
@Entity
@JsonIgnoreProperties(ignoreUnknown = true)
@Table(name = "phone")
public class Phone {

    /**
     * The unique identifier for the phone entity.
     * <p>
     * This field is auto-generated by the database.
     * </p>
     */
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private UUID id;

    /**
     * The phone number associated with the phone entity.
     */
    private String number;

    /**
     * The city code associated with the phone number.
     */
    private String cityCode;

    /**
     * The country code associated with the phone number.
     */
    private String countryCode;
}